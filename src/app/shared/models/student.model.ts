import * as t from 'io-ts';
import { Maybe, Option, LocalDateTimeFromString } from './common-types';

const Student = t.type({
  Id: t.number,
  AddressLine1: t.string,
  AddressLine2: Maybe(t.string),
  Birthdate: LocalDateTimeFromString,
  DisplayEmail: Option(t.string),
  // FirstName: t.string,
  FullName: t.string,
  Gender: t.union([t.literal('M'), t.literal('F'), t.literal('X')]),
  // LastName: t.string,
  Location: t.string,
  PhoneMobile: Option(t.string),
  PhonePrivate: Option(t.string),
  PostalCode: t.string,
  // HRef: t.string
});
type Student = t.TypeOf<typeof Student>;
export { Student };
